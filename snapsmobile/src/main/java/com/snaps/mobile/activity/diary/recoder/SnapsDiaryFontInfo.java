public class SnapsDiaryFontInfo {
    private String fontFace = null;
    private String fontSize = null;
    private String textControlWidth = null;

    public String getFontFace() {
        return fontFace;
    }

    public void setFontFace(String fontFace) {
        this.fontFace = fontFace;
    }

    public String getFontSize() {
        return fontSize;
    }

    public void setFontSize(String fontSize) {
        this.fontSize = fontSize;
    }

    public String getTextControlWidth() {
        return textControlWidth;
    }

    public void setTextControlWidth(String textControlWidth) {
        this.textControlWidth = textControlWidth;
    }
}
